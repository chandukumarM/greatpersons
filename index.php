 <div id="menu2" class="tab-pane fade text-justify">
   <h3>Rivers</h3>
   <p id="b2">The state has two major rivers flowing through it and they are Godavari and Krishna.

Godavari flows through Maharashtra, Madhya Pradesh, Karnataka and Orissa as well. It springs from the Western Ghats in Maharashtra, but the river receives most of its water not from the Western Ghats, but in the lower reaches. It flows into the Bay of Bengal.

Krishna is the second largest river and it flows from Maharashtra for 780kms before it reaches Andhra Pradesh. There are other small rivers such as Pennar, Nagavali, Vamsadhara, Tungabhadra, Manjeera, Swarnamukji, Pranahita etc.</p>
